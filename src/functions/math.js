/**
 * Function to get the sqrt root of a number
 * @param {Integer} a
 * @returns
 */
export const sqrt = (a) => {
  return Math.sqrt(a);
};

/**
 * This function sums twp numbers
 * @param {Integer} a
 * @param {Integer} b
 * @returns
 */
export function sum(a, b) {
  return a + b;
}
/**
 * function to calculate perimeter
 * @param {Number} a
 * @param {Number} b
 * @returns
 */
export const perimeter = (a, b) => {
  return 2 * a + 2 * b;
};

/**
 *
 * @param {Number} a
 * @param {Number} b
 * @returns
 */
export const substraction = (a, b) => {
  if (!a || !b) return null;
  return a - b;
};
